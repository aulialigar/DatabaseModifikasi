    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
        // TODO add your handling code here:
        if ("".equals(nis.getText()) || "".equals(alamat.getText()) || "".equals(kelas.getText()) || "".equals(nama.getText()) || "".equals(email.getText()) || "".equals(agama.getText()) || "".equals(anak.getText()) || "".equals(telepon.getText())) {
        JOptionPane.showMessageDialog(this, "Harap Lengkapi Data", "Error", JOptionPane.WARNING_MESSAGE);
        } else {
            String JK = "";
                if (laki.isSelected()) {
                    JK = "L";
                } else {
                    JK = "P";
                }
        String SQL = "UPDATE t_siswa SET"
                + "WHERE NamaSiswa='"+nama.getText()+"',"
                + "WHERE JenisKelamin='"+JK+"',"
                + "WHERE Kelas='"+kelas.getText()+"',"
                + "WHERE Email='"+email.getText()+"'"
                + "WHERE Alamat='"+alamat.getText()+"'"
                + "WHERE NIS='"+nis.getText()+"'"
                + "WHERE Agama='"+agama.getText()+"'"
                + "WHERE AnakKe='"+anak.getText()+"'"
                + "WHERE Telepon='"+telepon.getText()+"'";
        int status = KoneksiDB.execute(SQL);
        if (status == 0) {
            JOptionPane.showMessageDialog(this, "Data berhasil diupdate", "Sukses", JOptionPane.INFORMATION_MESSAGE);
            selectData();
        } else {
            JOptionPane.showMessageDialog(this, "Data gagal diupdate", "Gagal", JOptionPane.WARNING_MESSAGE);
        }
      }
    }//GEN-LAST:event_editActionPerformed

    private void dataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_dataMouseClicked
        // TODO add your handling code here:
        int baris = data.getSelectedRow();
        if (baris != -1) {
            nis.setText(data.getValueAt(baris, 0).toString());
            nama.setText(data.getValueAt(baris, 1).toString());
            if ("Laki-Laki".equals(data.getValueAt(baris, 2).toString())) {
                laki.setSelected(true);
            } else {
                perempuan.setSelected(true);
            }
            kelas.setText(data.getValueAt(baris, 3).toString());
            agama.setText(data.getValueAt(baris, 4).toString());
            anak.setText(data.getValueAt(baris, 5).toString());
            telepon.setText(data.getValueAt(baris, 6).toString());
            email.setText(data.getValueAt(baris, 7).toString());
            alamat.setText(data.getValueAt(baris, 8).toString());
        }
    }//GEN-LAST:event_dataMouseClicked

